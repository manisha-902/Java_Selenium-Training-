package day3;

public class Patient {
	private String name;
    private int age;
    private String healthInfo; 
    
    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }
    public void setHealthInfo(String info) {
        this.healthInfo = info;
    }

}
